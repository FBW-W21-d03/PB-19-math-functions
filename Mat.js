const ersteZahl=45;
const zweitezahl=48;
const dritteZahl= 7;
//+
function add() {
    let summe= ersteZahl+zweitezahl+dritteZahl;
    console.log(summe)
}
//-
function sub() {
    let summe=ersteZahl-zweitezahl-dritteZahl;
    console.log(summe)
}
//*
function mult() {
    let summe=ersteZahl*zweitezahl*dritteZahl;
    console.log(summe)
}
//"/"
function div() {
    let summe=ersteZahl/zweitezahl/dritteZahl;
    console.log(summe)
}

add();
sub();
mult();
div();