const firstNumber = 2;
const secondNumber = 3;
const thirdNumber = 1;

function addition() {
    let result1 = firstNumber + secondNumber + thirdNumber;
    console.log("addition",result1);
}
function subtraktion() {
let result2 = firstNumber - secondNumber - thirdNumber;
console.log("subtraktion", result2);
}

function muliplikation(){
    let result3 = firstNumber * secondNumber * thirdNumber;
    console.log("muliplikation",result3);
}
function division(){
    let result4 = firstNumber/secondNumber/thirdNumber;
    console.log("division",result4);
}


