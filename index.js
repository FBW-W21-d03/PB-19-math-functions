

const ersteZahl = 2;
const zweiteZahl = 3;
const driteZahl = 1;


function addition() {
  let resultat = ersteZahl + zweiteZahl + driteZahl;
  console.log("addition",resultat);
}

function substraktion() {
  let substraktion = ersteZahl - zweiteZahl - driteZahl;
  console.log("Die substraktion ist:",substraktion);

} 

function multiplikation() {
  let multiplikation = ersteZahl * zweiteZahl * driteZahl;
  console.log("multiplikation",multiplikation);
}

function dividieren() {
  let dividieren = ersteZahl / zweiteZahl / driteZahl;
  console.log("dividieren",dividieren);
}

addition();
substraktion();
multiplikation();
dividieren();




